public class Day08C {
    public static void main(String[] args) {
        byte num1 = 32;
        byte num2 = 82;
        
        if (num1 > num2) {
            System.out.println(num1+" is greater than "+num2);
        } else if (num1 < num2) {
            System.out.println(num1+" is less than "+num2);
        } else {
            System.out.println("Both numbers are the same");
        }
    }
}
