package mattrosejavab1compiled;

// additional increment decrement samples
public class Day05F {
    public static void main(String[] args) {
        int num1 = 1;
        
        System.out.println(num1++);
        System.out.println(num1++);
        System.out.println(num1++);
        System.out.println(num1++);
        System.out.println(num1++);
        num1 = 5;
        System.out.println(num1--);
        System.out.println(num1--);
        System.out.println(num1--);
        System.out.println(num1--);
        System.out.println(num1--);
    }
}
