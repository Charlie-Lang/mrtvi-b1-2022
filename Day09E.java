
package Week2;

// While Loop
public class Day09E {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println("Current number: "+i);
            i++;
        }
        System.out.println("----------------");
        i = 20;
        while (i > 10) {            
            System.out.println("Counting down from:"+i);
            i--;
        }
    }
}
