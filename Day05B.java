package mattrosejavab1compiled;

// using numeric data types and arithmetic operators
public class Day05B {
    public static void main(String[] args) {
        double num1 = 34;
        double num2 = 2.9;
        double result;
        result = num1 + num2;
        System.out.println(num1 + " + " + num2 + " = " + result);
        
        result = num1 - num2;
        System.out.println(num1 + " - " + num2 + " = " + result);
        
        result = num1 * num2;
        System.out.println(num1 + " * " + num2 + " = " + result);
        
        result = num1 / num2;
        System.out.println(num1 + " / " + num2 + " = " + result);
    }
}
